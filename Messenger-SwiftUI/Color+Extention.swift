import SwiftUI

extension Color {
    static var background: Color {
        return Color("background")
    }
    static var receiverBubble: Color {
        return Color("receiverBubble")
    }
    static var senderBubble: Color {
        return Color("senderBubble")
    }
    static var shadow: Color {
        return Color("shadow")
    }
}
